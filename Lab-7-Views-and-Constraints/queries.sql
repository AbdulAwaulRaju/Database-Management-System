CREATE TABLE CITIZEN (
    CITIZEN_ID INT AUTO_INCREMENT,
    CITIZEN_NAME VARCHAR(50) NOT NULL,
    CITIZEN_HOME VARCHAR(50),
    AGE INT CHECK (AGE >= 18),
    OCCUPATION VARCHAR(50),
    GENDER VARCHAR(10) CHECK (GENDER IN ('Male', 'Female')),
    SALARY FLOAT,
    CONSTRAINT PK_CITIZEN PRIMARY KEY (CITIZEN_ID)
);

CREATE TABLE PRODUCTS(
    PRODUCT_ID INT AUTO_INCREMENT,
    PRODUCT_NAME VARCHAR(15) NOT NULL UNIQUE,
    AMOUNT INT,
    PRICE FLOAT,
    CONSTRAINT PK_PRODUCT PRIMARY KEY(PRODUCT_ID),
    CONSTRAINT CHK_PRODUCT_AMOUNT CHECK (AMOUNT >= 0),
    CONSTRAINT CHK_PRODUCT_PRICE CHECK (PRICE > 0.0)
);

