CREATE VIEW STUDENT_CUSTOMER AS
SELECT *
FROM CUSTOMERS
WHERE OCCUPATION = 'Student';

CREATE VIEW MALE_CUSTOMER AS
SELECT C.CUSTOMER_ID,
       C.CUSTOMER_NAME,
       C.CITIZEN_ID,
       C.AGE,
       C.OCCUPATION
FROM CUSTOMERS C
JOIN CITIZEN Z
ON C.CITIZEN_ID = Z.CITIZEN_ID
WHERE Z.GENDER = 'Male';

SELECT DISTINCT P.PRODUCT_NAME
FROM STUDENT_CUSTOMER SC
JOIN ORDERS O
ON SC.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDER_DETAILS OD
ON O.ORDER_ID = OD.ORDER_ID
JOIN PRODUCTS P
ON OD.PRODUCT_ID = P.PRODUCT_ID;

SELECT DISTINCT P.PRODUCT_NAME
FROM MALE_CUSTOMER MC
JOIN ORDERS O
ON MC.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDER_DETAILS OD
ON O.ORDER_ID = OD.ORDER_ID
JOIN PRODUCTS P
ON OD.PRODUCT_ID = P.PRODUCT_ID;

CREATE VIEW OCCUPATION_PRODUCT_COUNT AS
SELECT C.OCCUPATION,
       COUNT(DISTINCT OD.PRODUCT_ID) AS TOTAL_PRODUCTS
FROM CUSTOMERS C
JOIN ORDERS O
ON C.CUSTOMER_ID = O.CUSTOMER_ID
JOIN ORDER_DETAILS OD
ON O.ORDER_ID = OD.ORDER_ID
GROUP BY C.OCCUPATION;

SELECT P.PRODUCT_NAME,
       COUNT(*) AS TOTAL_ORDERS
FROM ORDER_DETAILS OD
JOIN PRODUCTS P
ON OD.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_NAME;

SELECT PRODUCT_NAME
FROM (
    SELECT P.PRODUCT_NAME,
           COUNT(*) AS TOTAL_ORDERS
    FROM ORDER_DETAILS OD
    JOIN PRODUCTS P
    ON OD.PRODUCT_ID = P.PRODUCT_ID
    GROUP BY P.PRODUCT_NAME
    ORDER BY TOTAL_ORDERS DESC
    LIMIT 1
) AS TOP_PRODUCT;
