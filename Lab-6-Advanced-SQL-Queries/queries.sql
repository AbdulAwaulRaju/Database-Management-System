SELECT C_HOME, COUNT(*) AS num_customers
FROM CUSTOMERS
GROUP BY C_HOME;

SELECT C.C_NAME, SUM(P.PRICE) AS total_payment
FROM CUSTOMERS C
JOIN ORDERS O ON C.C_ID = O.C_ID
JOIN ORDER_DETAILS OD ON O.O_ID = OD.O_ID
JOIN PRODUCTS P ON OD.P_ID = P.P_ID
GROUP BY C.C_NAME
HAVING SUM(P.PRICE) > 12000;

