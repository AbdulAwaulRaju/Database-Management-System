SELECT AVG(P.PRICE) AS average_price
FROM CUSTOMERS C
JOIN ORDERS O ON C.C_ID = O.C_ID
JOIN ORDER_DETAILS OD ON O.O_ID = OD.O_ID
JOIN PRODUCTS P ON OD.P_ID = P.P_ID
