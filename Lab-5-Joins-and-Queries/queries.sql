CREATE TABLE CUSTOMERS (
	C_ID INT,
	C_NAME VARCHAR(10),
	C_HOME VARCHAR(10),
	CONSTRAINT PK_CITIZEN PRIMARY KEY (C_ID)
);

CREATE TABLE PRODUCTS (
	P_ID INT,
	P_NAME VARCHAR(15),
	AMOUNT INT,
	PRICE FLOAT,
	CONSTRAINT PK_PRODUCT PRIMARY KEY (P_ID)
);

CREATE TABLE ORDERS (
	O_ID INT,
	C_ID INT,
	CONSTRAINT PK_ORDER PRIMARY KEY (O_ID),
	CONSTRAINT fk_order_customer FOREIGN KEY(C_ID)
	REFERENCES CUSTOMERS(C_ID)
);

CREATE TABLE ORDER_DETAILS (
	O_ID INT,
	P_ID INT,
	CONSTRAINT PK_ORDER_DETAILS PRIMARY KEY (O_ID,P_ID),
	CONSTRAINT fk_orderDetails_order FOREIGN KEY(O_ID)
	REFERENCES ORDERS(O_ID),
	CONSTRAINT fk_orderDetails_product FOREIGN KEY(P_ID)
	REFERENCES PRODUCTS(P_ID)
);

